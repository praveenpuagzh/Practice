package org.inhertance;

public class Animlas extends Carnivors {

	/*In Multi-Level Inheritance in Java, a class extends to another 
	class that is already extended from another class.*/
	
	public static void main(String[] args) {
		
		Animlas q = new Animlas();
		q.lion();
		q.tiger();
		q.fox();
		q.cat();
		q.crow();
		q.dog();
		q.giraffe();
		q.hippotamas();
		q.deer();
	}
}
